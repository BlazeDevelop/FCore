package dev.flaymie.fcore.core.security;

/**
 * Статусы верификации плагина системой безопасности
 */
public enum PluginVerificationStatus {
    /**
     * Плагин не проверен
     */
    UNKNOWN,
    
    /**
     * Плагин успешно прошел проверку
     */
    VERIFIED,
    
    /**
     * Плагин имеет предупреждения, но может быть загружен
     */
    WARNING,
    
    /**
     * Плагин не прошел проверку и не может быть загружен
     */
    BLOCKED,
    
    /**
     * Плагин имеет измененную подпись или хеш
     */
    TAMPERED
} 